package Day2;

public class Mutant extends Person {
    public void changeFace() {
        System.out.println("My face changed");
    }

    public void Fly() {
        System.out.println("Flying in the sky.");
    }

    public void meltIron() {
        System.out.println("Melting Iron");
    }

    public void Freeze() {
        System.out.println("Freezing Objects");
    }

    public void Eat() {
        System.out.println("Eating Diamonds");
    }
}
